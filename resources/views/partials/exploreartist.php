    <h2 class="text-3xl font-semibold text-gray-600 mb-8 pt-12">EVERYONE IS AN ARTIST</h2>
    <p class="text-lg md:text-xl mb-6">
        Everyone is an artist in their own way, whether they realize it or not. Art is not confined to galleries or concert halls—it exists in the way we arrange our spaces, tell stories, cook meals, or capture moments in a photograph. Creativity is woven into everyday life, shaping how we see and interact with the world. The ability to express, to innovate, and to transform ideas into something tangible is not reserved for the few—it belongs to everyone. You do not need a title or an audience to be an artist; you only need the courage to create.
    </p>
